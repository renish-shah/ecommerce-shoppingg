package com.pragiti.domain;

public class CreditCardDetails {
	
	private String nameOnCard;
	private String creditcard_number;
	private String expyear;
	private String expmonth;
	private String cvv_number;
	
	
	
	public String getNameOnCard() {
		return nameOnCard;
	}
	public void setNameOnCard(String nameOnCard) {
		this.nameOnCard = nameOnCard;
	}
	public String getCreditcard_number() {
		return creditcard_number;
	}
	public void setCreditcard_number(String creditcard_number) {
		this.creditcard_number = creditcard_number;
	}
	public String getExpyear() {
		return expyear;
	}
	public void setExpyear(String expyear) {
		this.expyear = expyear;
	}
	public String getExpmonth() {
		return expmonth;
	}
	public void setExpmonth(String expmonth) {
		this.expmonth = expmonth;
	}
	public String getCvv_number() {
		return cvv_number;
	}
	public void setCvv_number(String cvv_number) {
		this.cvv_number = cvv_number;
	}
	
 
}
